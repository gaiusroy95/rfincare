const PLACEHOLDER_URL = 'https://www.irdai.gov.in';

async function seed() {
  const pool = getPool();

  for (const product of SAMPLE_PRODUCTS) {
    const [[existing]] = await pool.execute(
      `SELECT id FROM insurance_products WHERE slug = :slug LIMIT 1`,
      { slug: product.slug },
    );
    if (existing) {
      console.log(`  ↷ Skipped (exists): ${product.name}`);
      continue;
    }

    const id = randomUUID();
    await pool.execute(
      `INSERT INTO insurance_products (
        id, insurer_id, insurer_name, name, slug, description, logo_url, segment, categories,
        premium_from, premium_to, premium_unit, sum_insured_from, sum_insured_to,
        coverage_term_years, waiting_period_days, claim_settlement_ratio, cashless_hospitals,
        tax_benefit_80c, tax_benefit_80d,
        supports_new_policy, supports_renewal, supports_claim_assistance,
        new_policy_url, renewal_url, claim_assistance_url,
        features, benefits, highlights, display_priority, status
      ) VALUES (
        :id, NULL, :insurerName, :name, :slug, NULL, NULL, :segment, :categories::jsonb,
        :premiumFrom, :premiumTo, 'yearly', :sumInsuredFrom, :sumInsuredTo,
        :coverageTermYears, :waitingPeriodDays, :claimSettlementRatio, :cashlessHospitals,
        :taxBenefit80c, :taxBenefit80d,
        :supportsNewPolicy, :supportsRenewal, :supportsClaimAssistance,
        :newPolicyUrl, :renewalUrl, :claimUrl,
        :features, '[]', :highlights, :displayPriority, 'active'
      )`,
      {
        id,
        insurerName: product.insurerName,
        name: product.name,
        slug: product.slug,
        segment: product.segment,
        categories: JSON.stringify(product.categories),
        premiumFrom: product.premiumFrom ?? null,
        premiumTo: product.premiumTo ?? null,
        sumInsuredFrom: product.sumInsuredFrom ?? null,
        sumInsuredTo: product.sumInsuredTo ?? null,
        coverageTermYears: product.coverageTermYears ?? null,
        waitingPeriodDays: product.waitingPeriodDays ?? null,
        claimSettlementRatio: product.claimSettlementRatio ?? null,
        cashlessHospitals: product.cashlessHospitals ?? null,
        taxBenefit80c: product.taxBenefit80c ?? false,
        taxBenefit80d: product.taxBenefit80d ?? false,
        supportsNewPolicy: product.supportsNewPolicy ?? true,
        supportsRenewal: product.supportsRenewal ?? false,
        supportsClaimAssistance: product.supportsClaimAssistance ?? false,
        newPolicyUrl: PLACEHOLDER_URL,
        renewalUrl: product.supportsRenewal ? PLACEHOLDER_URL : null,
        claimUrl: product.supportsClaimAssistance ? PLACEHOLDER_URL : null,
        features: JSON.stringify(product.features || []),
        highlights: product.highlights || null,
        displayPriority: product.displayPriority ?? 0,
      },
    );
    console.log(`  ✓ Created: ${product.name}`);
  }

  console.log('\nDone seeding insurance products.');
  process.exit(0);
}

seed().catch((err) => {
  console.error(err);
  process.exit(1);
});
